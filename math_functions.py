
def addition(x, y):
    new_number = x + y
    return new_number

def subtraction(x, y):
    new_number = x - y
    return new_number

def division(x, y):
    new_number = x/y
    return new_number

def exponent(x,y):
    new_number = x^y
    return new_number

def multiplication(x,y):
    new_number = x*y
    return new_number
